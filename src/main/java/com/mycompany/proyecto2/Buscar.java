void llenarBusqueda(String numSerie) {
    try {
        String estado= "Desactivo";
        // Define los t√≠tulos de las columnas
        String titulos[] = {"Equipo", "Numero de serie", "Sucedido", "Aula", "Detalles", "Ingresado por", "Fecha de registro", "Estado"};
        DefaultTableModel model = new DefaultTableModel(null, titulos);

        // Consulta SQL para obtener los datos deseados
        String query = "SELECT c.Equipo, dr.Num_serie, dr.Id_aula, a.Nom_aula, dr.Detalle_reporte, " +
                       "emp.Nombres, dr.Fecha_registro, dr.Estado_activo " +
                       "FROM detalle_reporte dr " +
                       "JOIN componentes c ON dr.Id_equipo = c.Id_equipo " +
                       "JOIN aulas a ON dr.Id_aula = a.Id_aula " +
                       "JOIN empleados emp ON dr.Id_empleado = emp.Id_empleado " +
                       "WHERE dr.Num_serie = ?";

        // Crear un PreparedStatement para ejecutar la consulta
        PreparedStatement ps = con.prepareStatement(query);
        ps.setString(1, numSerie);
        ResultSet rs = ps.executeQuery();

        // Recorrer los resultados y llenar el modelo de la tabla
        while (rs.next()) {
            String fila[] = new String[8];
            if (rs.getInt("Estado_activo")!=0) {
                estado ="Activo";
            }
            fila[0] = rs.getString("Equipo"); // Verifica que coincida con el nombre de la columna en tu base de datos
            fila[1] = rs.getString("Num_serie");    // Verifica que coincida con el nombre de la columna en tu base de datos
            fila[2] = rs.getString("Id_aula");      // Verifica que coincida con el nombre de la columna en tu base de datos
            fila[3] = rs.getString("Nom_aula");  // Verifica que coincida con el nombre de la columna en tu base de datos
            fila[4] = rs.getString("Detalle_reporte"); // Verifica que coincida con el nombre de la columna en tu base de datos
            fila[5] = rs.getString("Nombres");       // Verifica que coincida con el nombre de la columna en tu base de datos
            fila[6] = rs.getString("Fecha_registro"); // Verifica que coincida con el nombre de la columna en tu base de datos
            fila[7] = estado; // Verifica que coincida con el nombre de la columna en tu base de datos
            model.addRow(fila);
        }

        // Asigna el modelo a la JTable
        tblBuscar.setModel(model);

    } catch (Exception e) {
        
        JOptionPane.showMessageDialog(null, "Error al buscar los datos: " + e.getMessage());
    }
}


    
    
